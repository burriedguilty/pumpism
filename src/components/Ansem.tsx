interface AnsemProps {
  waveFrequency?: number;
  waveAmplitude?: number;
}

const Ansem: React.FC<AnsemProps> = ({ waveFrequency = 0.1, waveAmplitude = 10 }) => {
  const [isSeizure, setIsSeizure] = useState(false);
  const containerRef = useRef<HTMLDivElement>(null);
  const [waveOffset, setWaveOffset] = useState(0);

  useEffect(() => {
    const seizureInterval = setInterval(() => {
      setIsSeizure(prev => !prev);
    }, 50);

    const waveInterval = setInterval(() => {
      setWaveOffset(prev => (prev + waveFrequency) % (2 * Math.PI));
    }, 16); // ~60fps

    return () => {
      clearInterval(seizureInterval);
      clearInterval(waveInterval);
    };
  }, [waveFrequency]);

  useEffect(() => {
    if (containerRef.current) {
      const containerWidth = containerRef.current.offsetWidth;
      const containerHeight = containerRef.current.offsetHeight;
      const fontSize = Math.min(containerWidth / 100, containerHeight / 100);
      containerRef.current.style.fontSize = `${fontSize}px`;
    }
  }, []);

  const getRowTransform = (index: number) => {
    const y = Math.sin(waveOffset + index * 0.1) * waveAmplitude;
    return `translateY(${y}px)`;
  };

  return (
    <div ref={containerRef} className="fixed inset-0 bg-black overflow-hidden flex items-center justify-center">
      <motion.pre
        className="whitespace-pre font-mono text-[#FF0000]"
        animate={{
          opacity: isSeizure ? [1, 0.3, 1] : 1,
          scale: isSeizure ? [1, 1.1, 0.9, 1] : 1,
          x: isSeizure ? [0, 10, -10, 0] : 0,
          y: isSeizure ? [0, -10, 10, 0] : 0,
        }}
        transition={{ duration: 0.1, times: [0, 0.2, 0.5, 1] }}
      >
        {asciiArt.split('\n').map((row, index) => (
          <motion.div
            key={index}
            style={{ transform: getRowTransform(index) }}
            transition={{ type: 'spring', stiffness: 100, damping: 5 }}
          >
            {row}
          </motion.div>
        ))}
      </motion.pre>
    </div>
  );
};

export default Ansem;